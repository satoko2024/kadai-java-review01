
public class ArithmeticOperations2 {

    public static void main(String[] args) {
        int num1 = 100;
        int num2 = 10;

        //resultで加算結果を表示
        int result = num1 + num2;
        System.out.println("num1 + num2 =" + result);

        //resultで除算結果を表示
        result = num1-num2;
        System.out.println("num1 - num2 = " + result);

        //同乗算結果
        result = num1 * num2;
        System.out.println("num1 * num2 = " + result);

        //同除算結果
        result = num1 / num2;
        System.out.println("num1 / num2 = " + result);

        //同残り％
        result = num1 % num2;
        System.out.println("num1 % num2 = " + result);

    }

}
